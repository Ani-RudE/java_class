public class singleDimensionalArray {
     public static void main(String[] args) {
          int roll[] = new int[5];
          roll[0] = 5;
          roll[1] = 15;
          roll[2] = 25;
          roll[3] = 35;
          roll[4] = 45;

          for (int i = 0; i < roll.length; i++) {
               System.out.print(roll[i] + " ");
          }
     }
}